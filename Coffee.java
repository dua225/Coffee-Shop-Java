// Coffee.java
public class Coffee {
    // private fields : encapsulation
    private String name;
    private double price;

    // Constructor
    public Coffee(String name, double price) {
        this.name = name;
        this.price = price;
    }

    // Getter for name
    public String getName() {
        return name;
    }

    // Getter for price
    public double getPrice() {
        return price;
    }
}
